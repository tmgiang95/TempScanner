package com.poa.tempscanner.mips;

public class ConfigParameters {
    private String companyName;

    private String deviceId;

    private String icNo;

    private String idCardNo;

    private String tricolorLamp;

    public String getCompanyName() {
        return this.companyName;
    }

    public String getDeviceId() {
        return this.deviceId;
    }

    public String getIcNo() {
        return this.icNo;
    }

    public String getIdCardNo() {
        return this.idCardNo;
    }

    public String getTricolorLamp() {
        return this.tricolorLamp;
    }

    public void setCompanyName(String paramString) {
        this.companyName = paramString;
    }

    public void setDeviceId(String paramString) {
        this.deviceId = paramString;
    }

    public void setIcNo(String paramString) {
        this.icNo = paramString;
    }

    public void setIdCardNo(String paramString) {
        this.idCardNo = paramString;
    }

    public void setTricolorLamp(String paramString) {
        this.tricolorLamp = paramString;
    }
}
